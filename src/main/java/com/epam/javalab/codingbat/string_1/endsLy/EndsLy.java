package com.epam.javalab.codingbat.string_1.endsLy;

public class EndsLy {
    /**
     * Given a string, return true if it ends in "ly".

     endsLy("oddly") → true
     endsLy("y") → false
     endsLy("oddy") → false
     */

    public static void main(String[] args) {}

    public boolean endsLy(String str) {
        return str.endsWith("ly");
    }
}
